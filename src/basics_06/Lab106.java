package src.basics_06;

public class Lab106 {
	
	public static void main(String[] args) {
		
		int[] marks = new int[4];
		marks[0] = 98;
		marks[1] = 23;
		marks[2] = 85;
		marks[3] = 53;
		// 1D array
		int i=0;
		while(i<marks.length) {
			System.out.println(marks[i]);
			i++;
		}
		System.out.println("----End----");
	}

}
