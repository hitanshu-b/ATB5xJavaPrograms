package src.basics_03;

public class Lab040 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(++a);
        // Pre Increment
        // ++ in front of a variable like a > pre >
        // do increment first and then print

        int b = 10;
        System.out.println(b++);
        System.out.println(b);
        //Post Increment
        // after the variable > post >
        // First print then increment

    }
}
